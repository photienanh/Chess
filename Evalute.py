def evaluate_piece(gs, piece, square, end_game) -> int:
    piece_type = piece.piece_type
    mapping = []
    if piece_type == "p":
        mapping = pawnEvalWhite if gs.whiteToMove else pawnEvalBlack
    if piece_type == "K":
        mapping = knightEval
    if piece_type == "B":
        mapping = bishopEvalWhite if gs.whiteToMove else bishopEvalBlack
    if piece_type == "R":
        mapping = rookEvalWhite if gs.whiteToMove else rookEvalBlack
    if piece_type == "Q":
        mapping = queenEval
    if piece_type == "K":
        if end_game:
            mapping = (
                kingEvalEndGameWhite
                if gs.whiteToMove
                else kingEvalEndGameBlack
            )
        else:
            mapping = kingEvalWhite if gs.whiteToMove else kingEvalBlack

    return mapping[square]

def evaluate_board(board) -> float:
    total = 0
    end_game

    for row in board:
        for square in row:
            piece = board.piece_at(square)
            if not piece:
                continue

            value = piece_value[piece.piece_type] + evaluate_piece(piece, square, end_game)
            total += value if square[0] == "b" else -value
    return total
